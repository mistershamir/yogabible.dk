// Social Auto-Post — Yoga Bible
// Scheduled function that checks for new journal entries and creates
// draft social media posts for all connected accounts.
// Runs every 30 minutes. Checks journals.json for entries published
// since the last run. Creates one draft post per connected platform.
// Can also be triggered manually:
//   POST /.netlify/functions/social-auto-post
//   Body: { "slug": "specific-slug" } — force-create for a specific entry
// Configured in netlify.toml: schedule = "*/30 * * * *"

const fs = require('fs');
const path = require('path');
const https = require('https');
const { getDb, serverTimestamp } = require('./shared/firestore');
const { jsonResponse } = require('./shared/utils');

const POSTS_COLLECTION = 'social_posts';
const ACCOUNTS_COLLECTION = 'social_accounts';
const SYSTEM_DOC = 'system';
const AUTO_POST_DOC = 'social_auto_post';

exports.handler = async (event) => {
  const db = getDb();

  try {
    // Load journal entries
    const journalPath = path.join(__dirname, '..', '..', 'src', '_data', 'journals.json');
    let journalData;
    try {
      journalData = JSON.parse(fs.readFileSync(journalPath, 'utf8'));
    } catch (err) {
      console.log('[social-auto-post] Cannot read journals.json:', err.message);
      return jsonResponse(200, { ok: true, skipped: true, reason: 'journals.json not readable' });
    }

    const entries = journalData.entries || [];
    if (entries.length === 0) {
      return jsonResponse(200, { ok: true, created: 0 });
    }

    // Check for manual trigger with specific slug
    let targetSlug = null;
    if (event.httpMethod === 'POST' && event.body) {
      try {
        const body = JSON.parse(event.body);
        targetSlug = body.slug || null;
      } catch (e) { /* ignore */ }
    }

    // Get connected accounts
    const accountsSnap = await db.collection(ACCOUNTS_COLLECTION).get();
    const platforms = [];
    accountsSnap.forEach(doc => {
      const d = doc.data();
      if (d.accessToken) platforms.push(doc.id);
    });

    if (platforms.length === 0) {
      console.log('[social-auto-post] No connected accounts');
      return jsonResponse(200, { ok: true, created: 0, reason: 'no accounts' });
    }

    // Get last run timestamp
    let lastRun = null;
    if (!targetSlug) {
      try {
        const lastRunDoc = await db.collection(SYSTEM_DOC).doc(AUTO_POST_DOC).get();
        if (lastRunDoc.exists) {
          const d = lastRunDoc.data();
          lastRun = d.lastRun?.toDate ? d.lastRun.toDate() : d.lastRun ? new Date(d.lastRun) : null;
        }
      } catch (e) { /* first run */ }
    }

    // Get existing auto-generated post slugs to avoid duplicates
    const existingSnap = await db.collection(POSTS_COLLECTION)
      .where('autoGenerated', '==', true)
      .limit(200)
      .get();

    const existingSlugs = new Set();
    existingSnap.forEach(doc => {
      const d = doc.data();
      if (d.journalSlug) existingSlugs.add(d.journalSlug);
    });

    // Find new entries to post about
    const newEntries = entries.filter(entry => {
      if (targetSlug) return entry.slug === targetSlug;
      if (existingSlugs.has(entry.slug)) return false;
      if (!lastRun) return false; // First run: don't auto-post all old entries
      const entryDate = new Date(entry.date + 'T00:00:00Z');
      return entryDate >= lastRun;
    });

    if (newEntries.length === 0) {
      // Update last run even with no new entries
      await updateLastRun(db, 0);
      return jsonResponse(200, { ok: true, created: 0 });
    }

    let created = 0;

    for (const entry of newEntries) {
      // Skip if already has posts for this slug (double-check)
      if (!targetSlug && existingSlugs.has(entry.slug)) continue;

      // Generate social post content — try AI first, fall back to basic
      let postContent;
      for (const platform of platforms) {
        try {
          postContent = await generateSmartCaption(entry, platform);
        } catch (aiErr) {
          console.warn('[social-auto-post] AI caption failed, using basic:', aiErr.message);
          postContent = generatePostContent(entry);
        }

        const postData = {
          caption: postContent.caption,
          platforms: [platform],
          mediaUrls: postContent.mediaUrl ? [postContent.mediaUrl] : [],
          hashtags: postContent.hashtags,
          status: 'draft',
          scheduledAt: null,
          publishedAt: null,
          publishResults: {},
          createdAt: serverTimestamp(),
          updatedAt: serverTimestamp(),
          autoGenerated: true,
          journalSlug: entry.slug,
          journalTitle: entry.title_en || entry.title_da,
          mediaType: 'auto'
        };

        await db.collection(POSTS_COLLECTION).add(postData);
        created++;
      }

      existingSlugs.add(entry.slug);
    }

    await updateLastRun(db, created);

    console.log(`[social-auto-post] Created ${created} draft posts for ${newEntries.length} journal entries`);
    return jsonResponse(200, { ok: true, created, entries: newEntries.map(e => e.slug) });
  } catch (err) {
    console.error('[social-auto-post] Error:', err);
    return jsonResponse(500, { ok: false, error: err.message });
  }
};


function generatePostContent(entry) {
  const siteUrl = process.env.URL || 'https://yogabible.dk';
  const postUrl = `${siteUrl}/yoga-journal/${entry.slug}/`;

  // Use English excerpt as base caption (more universal for social)
  const excerpt = entry.excerpt_en || entry.excerpt_da || '';
  const title = entry.title_en || entry.title_da || '';

  // Build caption with title, excerpt, and link
  const caption = `${title}\n\n${excerpt}\n\n📖 Read more: ${postUrl}`;

  // Build hashtags from tags
  const hashtags = (entry.tags || [])
    .map(tag => '#' + tag.replace(/[^a-zA-Z0-9]/g, ''))
    .concat(['#yogabible', '#yoga', '#copenhagen']);

  // Use entry image if available, otherwise null
  let mediaUrl = null;
  if (entry.image && !entry.image.includes('og-1200x630')) {
    mediaUrl = entry.image.startsWith('http') ? entry.image : `${siteUrl}${entry.image}`;
  }

  return { caption, hashtags, mediaUrl };
}


async function updateLastRun(db, created) {
  try {
    await db.collection(SYSTEM_DOC).doc(AUTO_POST_DOC).set({
      lastRun: serverTimestamp(),
      lastCreated: created,
      updatedAt: serverTimestamp()
    });
  } catch (err) {
    console.warn('[social-auto-post] Could not update last run:', err.message);
  }
}


/**
 * Generate a platform-native caption using Claude AI.
 * Calls social-ai internally via the smart-blog-caption action pattern.
 */
async function generateSmartCaption(entry, platform) {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) throw new Error('No ANTHROPIC_API_KEY');

  const siteUrl = process.env.URL || 'https://yogabible.dk';
  const postUrl = `${siteUrl}/yoga-journal/${entry.slug}/`;
  const title = entry.title_en || entry.title_da || '';
  const excerpt = entry.excerpt_en || entry.excerpt_da || '';
  const tags = entry.tags || [];

  const prompt = `Write a native social media caption for ${platform} to promote this blog post.

Blog title: "${title}"
Blog excerpt: "${excerpt}"
Blog URL: ${postUrl}
Tags: ${tags.join(', ')}

Do NOT just copy the excerpt. Write a platform-native caption that:
- Opens with a compelling hook (question, bold statement, or surprising fact)
- Teases the blog content without giving everything away
- Ends with a CTA to read the full post (include the URL naturally)
- Feels like a genuine social media post, not a blog announcement
- NEVER mention course language or refund policies

Also provide 15-20 hashtags.

Respond with ONLY valid JSON:
{"caption": "The caption with \\n line breaks", "hashtags": ["#tag1", "#tag2"]}`;

  const response = await claudeRequest(apiKey, prompt);
  let parsed;
  try {
    parsed = JSON.parse(response);
  } catch (e) {
    const match = response.match(/\{[\s\S]*\}/);
    if (match) parsed = JSON.parse(match[0]);
    else throw new Error('Failed to parse AI response');
  }

  let mediaUrl = null;
  if (entry.image && !entry.image.includes('og-1200x630')) {
    mediaUrl = entry.image.startsWith('http') ? entry.image : `${siteUrl}${entry.image}`;
  }

  return {
    caption: parsed.caption || `${title}\n\n${excerpt}\n\n📖 ${postUrl}`,
    hashtags: parsed.hashtags || tags.map(t => '#' + t.replace(/[^a-zA-Z0-9]/g, '')),
    mediaUrl
  };
}


function claudeRequest(apiKey, userMessage) {
  return new Promise(function (resolve, reject) {
    const body = JSON.stringify({
      model: 'claude-sonnet-4-6',
      max_tokens: 1500,
      system: 'You are a social media manager for Yoga Bible, a yoga teacher training school in Copenhagen. Write engaging, authentic captions. Never mention course language or refund policies. Always respond with valid JSON only.',
      messages: [{ role: 'user', content: userMessage }]
    });

    const opts = {
      hostname: 'api.anthropic.com',
      path: '/v1/messages',
      method: 'POST',
      headers: {
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01',
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(body)
      }
    };

    const req = https.request(opts, function (res) {
      const chunks = [];
      res.on('data', function (c) { chunks.push(c); });
      res.on('end', function () {
        const raw = Buffer.concat(chunks).toString();
        try {
          const json = JSON.parse(raw);
          if (json.content && json.content[0]) {
            resolve(json.content[0].text);
          } else {
            reject(new Error('Claude API error: ' + raw.substring(0, 300)));
          }
        } catch (e) {
          reject(new Error('Claude parse error: ' + raw.substring(0, 300)));
        }
      });
    });
    req.on('error', reject);
    req.write(body);
    req.end();
  });
}
